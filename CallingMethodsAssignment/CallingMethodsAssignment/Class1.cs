using System;
using System.Collections.Generic;
using System.Text;

namespace CallingMethodsAssignment
{
    public class MathOperations
    {
        public int AddOne(int num)
        {
            return num + 1;
        }

        public int Squared(int num)
        {
            return num * num;
        }

        public int Double(int num)
        {
            return num * 2;
        }
    }
}
